    public void buscarCanciones(String filtro){
    
        //Eliminamos las canciones previamente desplegadas si existen        
        if(!cancionesDesplegadas.isEmpty())
            cancionesDesplegadas.clear();
        
        //Eliminamos las canciones que se estén mostrando en pantalla
        panelCanciones.removeAll();
        
        int counter = 0;
        
        //Buscamos las canciones con el filtro especificado
        try {
            
            List<AlbumDTO> albumes = new ArrayList<>();
            
            
            if(filtroMusica == null)
                    
                if(checkFechaLanzamiento.isSelected())
                    albumes = frmPrincipal.albumNegocio.obtenerCancionesPorFecha(filtro, frmPrincipal.getLoggedUser());
                
                else
                    albumes = frmPrincipal.albumNegocio.obtenerCancionesPorBusqueda(filtro, frmPrincipal.getLoggedUser());
            
            else

                if(checkFechaLanzamiento.isSelected())
                    albumes = frmPrincipal.albumNegocio.obtenerCancionesPorFechaGeneros(filtro, filtroMusica.getGeneros());
                
                else
                    albumes = frmPrincipal.albumNegocio.obtenerCancionesPorBusquedaGeneros(filtro, filtroMusica.getGeneros());
            
            //Si nos regresa datos vacíos, terminamos ejecución
            if(albumes == null)
                return;
            
            //Iteramos por cada album que nos regresa
            for(AlbumDTO album : albumes){
                
                //Iteramos por cada canción dentro del album
                for(CancionDoc cancion : album.getCanciones()){
                    
                    PanelCancionDesplegada panel = new PanelCancionDesplegada(frmPrincipal,this, cancion, album, frmPrincipal.getLoggedUser(), frmPrincipal.usuarioNegocio);
                    //Si ya están desplegadas más de 9 canciones terminamos la ejecución, ya que ya no hay espacio para mostrar
                    if(counter >= 9)
                        return;
                    
                    //Agregamos la canción desplegada en la lista de canciones
                    cancionesDesplegadas.add(cancion);
                    
                    panelCanciones.add(panel);
                    
                    counter++;
                    
                }
                
            }
                
        } catch (NegocioException ex) {
            JOptionPane.showMessageDialog(this, "Error al buscar las canciones " + ex);
        }
        
        
    }
    
